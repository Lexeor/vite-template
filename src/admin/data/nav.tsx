import type { ReactNode } from 'react';

export type NavLink = { label: string; to: string };
export type NavGroupChild = NavLink | { section: string };
export type NavLeaf = { label: string; to: string; end?: boolean; icon: ReactNode };
export type NavGroup = { label: string; icon: ReactNode; children: NavGroupChild[] };
export type NavItem = NavLeaf | NavGroup;

export function isGroup(item: NavItem): item is NavGroup {
  return 'children' in item;
}

export function isSection(child: NavGroupChild): child is { section: string } {
  return 'section' in child;
}

